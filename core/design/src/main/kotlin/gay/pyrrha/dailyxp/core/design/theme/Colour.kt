// https://github.com/catppuccin/catppuccin
interface CatppuccinPalette {
    val isDark: Boolean
        get() = true

    val rosewater: Color
    val flamingo: Color
    val pink: Color
    val mauve: Color
    val red: Color
    val maroon: Color
    val peach: Color
    val yellow: Color
    val green: Color
    val teal: Color
    val sky: Color
    val sapphire: Color
    val blue: Color
    val lavender: Color
    val text: Color
    val subtext1: Color
    val subtext0: Color
    val overlay2: Color
    val overlay1: Color
    val overlay0: Color
    val surface2: Color
    val surface1: Color
    val surface0: Color
    val base: Color
    val mantle: Color
    val crust: Color
}

fun CatppuccinPalette.asColorScheme(accent: Color): ColorScheme =
    if (isDark) {
        darkColorScheme(
            background = base,
            surface = base,
            onPrimary = crust,
            onSurface = text,
            surfaceVariant = surface0,
            onSurfaceVariant = text,
            error = red,
            primary = accent
        )
    } else {
        lightColorScheme(
            background = base,
            surface = base,
            onPrimary = crust,
            onSurface = text,
            surfaceVariant = surface0,
            onSurfaceVariant = text,
            error = red,
            primary = accent
        )
    }


